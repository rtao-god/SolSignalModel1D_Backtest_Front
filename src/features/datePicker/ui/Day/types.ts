/**
 * Контракт одной ячейки дня после расчёта календарной сетки и диапазонных состояний.
 */
export default interface DayProps {
    /** Дата ячейки или `null` для технического заполнителя сетки вне текущего месяца. */
    date: Date | null
    /** Дополнительный CSS-класс для внешнего контейнера ячейки. */
    className?: string
    /** Блокирует клик по датам вне допустимого окна выбора. */
    isDisabled: boolean
    /** Помечает текущую календарную дату, если она не перекрыта активным выбором. */
    isToday: boolean
    /** Дата выбрана как единственная стартовая точка без второй границы. */
    isStandaloneSelection: boolean
    /** Обе границы диапазона совпали в одной дате. */
    isSingleSelected: boolean
    /** Дата является началом диапазона. */
    isRangeStart: boolean
    /** Дата является концом диапазона. */
    isRangeEnd: boolean
    /** Дата лежит внутри диапазона и не является его явной границей. */
    isInRange: boolean
    /** Внутридиапазонный день совпал с первым днём месяца и требует отдельного визуального cap-состояния. */
    isRangeMonthStart: boolean
    /** Внутридиапазонный день совпал с последним днём месяца и требует отдельного визуального cap-состояния. */
    isRangeMonthEnd: boolean
    /** Вызывается только для реальных дат и передаёт выбранный день наверх в owner-контракт. */
    onSelectDate: (date: Date) => void
}
