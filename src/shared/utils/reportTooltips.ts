function normalizeTooltipSelfAliases(values: string[]): string[] {
    return Array.from(new Set(values.map(value => value.trim()).filter(value => value.length > 0)))
}

export function resolveReportTooltipSelfAliases(reportKind: string | undefined, title: string | undefined): string[] {
    if (!reportKind?.startsWith('current_prediction')) {
        return []
    }

    const normalizedTitle = normalizeKey(title).toLowerCase()
    if (!normalizedTitle) {
        return []
    }

    return normalizeTooltipSelfAliases(CURRENT_PREDICTION_TOOLTIP_SELF_ALIASES[normalizedTitle] ?? [])
}

