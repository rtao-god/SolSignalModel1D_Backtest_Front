function isDiagnosticsSpecificitySectionKey(key: string, sectionFamily: DiagnosticsSectionFamily): boolean {
    if (DIAGNOSTICS_SPECIFICITY_KEYS.has(key)) {
        return true
    }

    switch (sectionFamily) {
        case 'policy-specificity-split':
            return [
                'SpecificDays',
                'SpecificTrade%',
                'SpecificNoTrade%',
                'SpecificOpposite%',
                'NormalDays',
                'NormalTrade%',
                'NormalNoTrade%',
                'NormalOpposite%',
                'SpecificityUndefinedDays'
            ].includes(key)
        case 'specificity-global-thresholds':
            return ['Mode', 'DayExclusive', 'Samples', 'AbsRetP90', 'MinMoveP90', 'Defined'].includes(key)
        case 'specificity-rolling-guardrail':
            return [
                'Year',
                'Days',
                'AbsRetP90',
                'MinMoveP90',
                'AbsP90_vs_global_delta%',
                'MinMoveP90_vs_global_delta%'
            ].includes(key)
        case 'specificity-rolling-guardrail-causal':
            return [
                'Year',
                'Days',
                'DefinedDays',
                'LastSamples',
                'MinSamples',
                'UndefinedReason',
                'AbsRetP90_Last',
                'MinMoveP90_Last'
            ].includes(key)
        default:
            return false
    }
}

